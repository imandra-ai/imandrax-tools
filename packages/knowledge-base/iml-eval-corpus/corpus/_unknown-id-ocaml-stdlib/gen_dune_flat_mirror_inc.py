"""Emit dune stanzas that mirror each `_unknown-id-ocaml-stdlib/<name>/` dir
to a flat top-level `unknown-id-ocaml-stdlib-<name>/` dir (promoted), and
render each entry's `query/__main__.py` from the shared jinja template.

_unknown-id-ocaml-stdlib/a -> unknown-id-ocaml-stdlib-a
_unknown-id-ocaml-stdlib/b -> unknown-id-ocaml-stdlib-b
...

Run from this file's directory. Output goes to stdout; dune captures it
into `dune.flat_mirror.inc`.
"""

from pathlib import Path

SRC_GROUP = "_unknown-id-ocaml-stdlib"
FLAT_PREFIX = "unknown-id-ocaml-stdlib"
CURR_DIR = Path(__file__).parent


def main() -> None:
    group = CURR_DIR.parent / SRC_GROUP
    entries = sorted(p.name for p in group.iterdir() if p.is_dir())

    print(f"""\
; Generated by {Path(__file__).name}. Do not edit by hand.
; _unknown-id-ocaml-stdlib/a -> unknown-id-ocaml-stdlib-a
; _unknown-id-ocaml-stdlib/b -> unknown-id-ocaml-stdlib-b
; ...\
""")
    print()
    for name in entries:
        flat = f"{FLAT_PREFIX}-{name}"
        out = f"""\
(subdir {flat}
 (copy_files
  (mode (promote (until-clean)))
  (files ../{SRC_GROUP}/{name}/*.{{iml,json,md,scm}}))

 (subdir query
  (rule
   (mode (promote (until-clean)))
   (target __init__.py)
   (deps
    (:tmpl ../../{SRC_GROUP}/query.__init__.py.j2)
    (:meta ../../{SRC_GROUP}/meta.json))
   (action
    (run uvx --from jinja2-cli jinja2
         %{{tmpl}} %{{meta}}
         --format=json -s {name} -D name={name}
         -o %{{target}})))))
"""
        print(out)


if __name__ == "__main__":
    main()
