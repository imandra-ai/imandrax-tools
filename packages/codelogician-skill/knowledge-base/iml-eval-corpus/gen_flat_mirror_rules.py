"""Emit dune stanzas that mirror each `ocaml-std-unavailable/<name>/` dir
to a flat top-level `ocaml-stdlib-unknown-id-<name>/` dir (promoted).

Run from this file's directory. Output goes to stdout; dune captures it
into `dune.flat_mirror.inc`.
"""

from pathlib import Path

SRC_GROUP = "_unknown-id-ocaml-stdlib"
FLAT_PREFIX = "unknown-id-ocaml-stdlib"


def main() -> None:
    here = Path(__file__).parent
    group = here / SRC_GROUP
    entries = sorted(p.name for p in group.iterdir() if p.is_dir())

    print(f"; Generated by {Path(__file__).name}. Do not edit by hand.")
    print()
    for name in entries:
        flat = f"{FLAT_PREFIX}-{name}"
        out = f"""\
(subdir {flat}
 (copy_files
  (mode (promote (until-clean)))
  (files ../{SRC_GROUP}/{name}/*.{{iml,json,md,scm}})))"""
        print(out)


if __name__ == "__main__":
    main()
